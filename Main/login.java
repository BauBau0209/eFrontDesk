    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if((username.getText().equals("Tessa") || username.getText().equals("Bau") || username.getText().equals("Rhome") 
                || username.getText().equals("Karl")) 
                && password.getText().equals("admin123")){
            dashboard dashboardFrame = new dashboard(username.getText());
            dashboardFrame.setExtendedState(JFrame.MAXIMIZED_BOTH);
            dashboardFrame.setVisible(true);
            dispose();
        } else if(username.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill out username");
        } else if(password.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please fill out password");
        } else if (!(username.getText().equals("Tessa") || username.getText().equals("Bau") || username.getText().equals("Rhome") 
                || username.getText().equals("Karl"))){
            JOptionPane.showMessageDialog(null, "Incorrect Username");
        } else if (!(password.getText().equals("admin123"))){
            JOptionPane.showMessageDialog(null, "Incorrect Password");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

