    public emergencyContact(String[] patientData, String[] contactData, String[] insuranceData, boolean hasGuarantor){
        this();
        this.patientData = patientData;
        this.contactData = contactData;
        this.insuranceData = insuranceData;
        this.hasGuarantor = hasGuarantor;
        if (contactData != null) {
            eID1.setText(contactData[1]);
            eName1.setText(contactData[2]);
            eContactNo1.setText(contactData[3]);
            if(contactData.length > 4){
                eID2.setText(contactData[5]);
                eName2.setText(contactData[6]);
                eContactNo2.setText(contactData[7]);
            }
        }
    }
    
    private void idAutoIncrement(){
        try {
            dbConnect con = new dbConnect();
            Connection connection = con.Connect();
            String sqlQuery = "SELECT EmergencyContactID FROM efrontdesk.emercon ORDER BY EmergencyContactID DESC LIMIT 1";
            PreparedStatement pst = connection.prepareStatement(sqlQuery);
            ResultSet rs = pst.executeQuery();
            if(rs.next()){
                int id = rs.getInt(1);
                int n = id+1;
                eID1.setText(Integer.toString(n));
                eID1.setEditable(false);
                eID2.setText(Integer.toString(n+1));
                eID2.setEditable(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "There was an error: " + e);
        }
    }

