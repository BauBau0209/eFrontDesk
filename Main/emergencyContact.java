    private void NextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NextButtonActionPerformed
        if (eName1.getText().equals("") ||
            eContactNo1.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please complete the form");
            return;
        } else {
            if(!(eName2.getText().equals("") || eContactNo1.getText().equals(""))){
                String[] contactTextField = {
                    this.patientData[0],
                    eID1.getText(),
                    eName1.getText(),
                    eContactNo1.getText(),
                    this.patientData[0],
                    eID2.getText(),
                    eName2.getText(),
                    eContactNo2.getText()
                };
                this.contactData = contactTextField;
            } else {
                String[] contactTextField = {
                    this.patientData[0],
                    eID1.getText(),
                    eName1.getText(),
                    eContactNo1.getText()
                };
                this.contactData = contactTextField;
            }
        }
        
        insurance insuranceFrame = new insurance(this.patientData, this.contactData, this.insuranceData, this.hasGuarantor);
        insuranceFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        insuranceFrame.setLocationRelativeTo(null);
        insuranceFrame.setResizable(false);
        insuranceFrame.setVisible(true);
        dispose();
    }//GEN-LAST:event_NextButtonActionPerformed

    private void BackButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BackButtonActionPerformed
        if (!(eName1.getText().equals("") ||
            eContactNo1.getText().equals(""))) {
            if(!(eName2.getText().equals("") || eContactNo1.getText().equals(""))){
                String[] contactTextField = {
                    this.patientData[0],
                    eID1.getText(),
                    eName1.getText(),
                    eContactNo1.getText(),
                    this.patientData[0],
                    eID2.getText(),
                    eName2.getText(),
                    eContactNo2.getText()
                };
                this.contactData = contactTextField;
            } else {
                String[] contactTextField = {
                    this.patientData[0],
                    eID1.getText(),
                    eName1.getText(),
                    eContactNo1.getText()
                };
                this.contactData = contactTextField;
            }
        }       
        
        patientInfo patientInfoFrame = new patientInfo(this.patientData, this.contactData, this.insuranceData);
        patientInfoFrame.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        patientInfoFrame.setLocationRelativeTo(null);
        patientInfoFrame.setResizable(false);
        patientInfoFrame.setVisible(true);
        dispose();
    }//GEN-LAST:event_BackButtonActionPerformed

